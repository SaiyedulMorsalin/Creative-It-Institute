# Python Data Types
'''
Integers: Whole numbers without decimals.
Floats: Numbers with decimals.
Strings: Text enclosed in single or double quotes.
Booleans: Contains two built in values(True or False/ 1 or 0)
'''
integer_number = 100
float_number = 50.5
string_name = 'Creative IT Institute'
bool_value = True
print("Type of integer_number: ",type(integer_number))
print("\nType of float_number: ",type(float_number))
print("\nType of string_name: ",type(string_name))
print("\nType of bool_value: ",type(bool_value))
